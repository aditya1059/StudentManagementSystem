package students;

public class Student {
    private int roll;
    private String name;
    private String city;
    private int deptNo;

    public int getRoll() {
        return this.roll;
    }

    public String getName() {
        return this.name;
    }

    public String getCity() {
        return this.city;
    }

    public int getDeptNo() {
        return this.deptNo;
    }

    public void setRoll(int roll) {
        this.roll=roll;
    }

    public void setName(String name) {
        this.name=name;
    }

    public void setCity(String city) {
        this.city=city;
    }

    public void setDeptNo(int deptNo) {
        this.deptNo=deptNo;
    }


}
